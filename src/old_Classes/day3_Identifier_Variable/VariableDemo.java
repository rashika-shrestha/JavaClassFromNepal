public class VariableDemo {

    public static void main(String[] args) {
        //1. single variable declaration
        int a;
        //2.single variable initialization
        a = 5;

        //single variable declaration and initialization simultaneously
        int i = 5;

        //1. multiple variable declaration
        int x, y, z;
        //2.multiple variable initialization
        x = 58;
        y = 4;
        z = 6;

        //multiple variable declaration and initialization simultaneously
        int b = 5, c = 6, d = 2;

        //1. multiple variable declaration
        int m, n, o;
        //all variables have same value
        m = n = o = 7;

        int p = 5;
        //q has same value as p
        int q = p;
    }
}


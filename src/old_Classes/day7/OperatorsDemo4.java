package old_Classes.day7;

public class OperatorsDemo4 {

    public static void main(String[] args) {
        //Relational Operator > < >= <= !=
        int a = 4;
        int b = 5;
        System.out.println(a > b);//false
        System.out.println(a < b);//true
        System.out.println(a >= b);//false
        System.out.println(a <= b);//true
        System.out.println(a == b);//false
        System.out.println(a != b);//true
    }
}
